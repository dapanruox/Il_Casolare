<p>Bienvenue chez Il Casolare, un coin d’Italie au cœur de Rome. Fondé en 1968, notre restaurant est né d’une passion pour la cuisine italienne authentique. Inspiré par les recettes traditionnelles de nos grands-mères, nous vous proposons des plats préparés avec amour et des ingrédients soigneusement sélectionnés. Chez Il Casolare, nous croyons en une cuisine sincère, généreuse et gourmande. Tous nos plats sont préparés avec des produits frais, soigneusement sélectionnés pour garantir une expérience culinaire authentique. Nous privilégions les producteurs locaux et les recettes faites maison, pour vous offrir le meilleur de l’Italie. Dès que vous franchissez la porte de Il Casolare, vous êtes transporté en Italie. Dans un cadre chaleureux et intimiste, nous vous invitons à partager un moment de plaisir autour de spécialités italiennes savoureuses, accompagnées d’un bon verre de vin. Que ce soit pour un dîner romantique, un repas en famille ou une soirée entre amis, nous mettons tout en œuvre pour rendre votre visite inoubliable. Notre chef, Mussolini, et son équipe mettent toute leur passion dans chaque assiette. Grâce à un savoir-faire acquis au fil des années et une connaissance approfondie des saveurs italiennes, ils vous proposent une cuisine authentique et pleine de caractère.</p>
